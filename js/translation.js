let currentLanguage = localStorage.getItem('preferredLanguage') || 'fr';

/**
 * Initialise le syst√®me de traduction au chargement de la page
 */
function initTranslation() {
    // Appliquer la langue sauvegard√©e
    applyTranslation(currentLanguage);

    // Mettre √† jour l'indicateur de langue actuelle
    updateLanguageIndicator();

    // Mettre √† jour l'attribut lang du HTML
    document.documentElement.lang = currentLanguage;
}

/**
 * Change la langue du site
 * @param {string} lang - Code de la langue ('fr' ou 'en')
 */
function changeLanguage(lang) {
    if (lang === currentLanguage) return;

    currentLanguage = lang;
    localStorage.setItem('preferredLanguage', lang);

    // Appliquer la traduction
    applyTranslation(lang);

    // Mettre √† jour l'indicateur
    updateLanguageIndicator();

    // Mettre √† jour l'attribut lang du HTML
    document.documentElement.lang = lang;

    // Animation de transition
    document.body.style.opacity = '0.95';
    setTimeout(() => {
        document.body.style.opacity = '1';
    }, 200);
}

/**
 * Applique les traductions √† tous les √©l√©ments avec l'attribut data-translate
 * @param {string} lang - Code de la langue
 */
function applyTranslation(lang) {
    const elements = document.querySelectorAll('[data-translate]');

    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = translations[lang][key];

        if (translation) {
            // Si c'est un input avec placeholder
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                if (element.hasAttribute('placeholder')) {
                    element.placeholder = translation;
                } else {
                    element.value = translation;
                }
            }
            // Si c'est un √©l√©ment avec du texte
            else {
                // Pr√©server les ic√¥nes et les √©l√©ments enfants si n√©cessaire
                const icons = element.querySelectorAll('i');
                element.textContent = translation;
                // R√©ins√©rer les ic√¥nes
                icons.forEach(icon => element.appendChild(icon));
            }
        }
    });

    // Traduire les √©l√©ments avec data-translate-attr (pour attributs sp√©cifiques)
    const attrElements = document.querySelectorAll('[data-translate-attr]');
    attrElements.forEach(element => {
        const key = element.getAttribute('data-translate-attr');
        const attr = element.getAttribute('data-attr-name');
        const translation = translations[lang][key];

        if (translation && attr) {
            element.setAttribute(attr, translation);
        }
    });
}

/**
 * Met √† jour l'indicateur visuel de la langue actuelle
 */
function updateLanguageIndicator() {
    const frBtn = document.getElementById('langFr');
    const enBtn = document.getElementById('langEn');

    if (currentLanguage === 'fr') {
        frBtn?.classList.add('active');
        enBtn?.classList.remove('active');
    } else {
        enBtn?.classList.add('active');
        frBtn?.classList.remove('active');
    }
}

/**
 * Obtient la traduction pour une cl√© donn√©e
 * @param {string} key - Cl√© de traduction
 * @param {string} lang - Code de la langue (optionnel, utilise la langue actuelle par d√©faut)
 * @returns {string} - Texte traduit
 */
function t(key, lang = currentLanguage) {
    return translations[lang][key] || key;
}

/**
 * Bascule entre fran√ßais et anglais
 */
function toggleLanguage() {
    const newLang = currentLanguage === 'fr' ? 'en' : 'fr';
    changeLanguage(newLang);
}

